numes = [2,3,4,5,6,7] ## listas

print(numes) ## printar a lista inteira

print(numes[0]) ## printar apenas 1 index

numes[0] = 1 ## trocar conteúdo da variável direto do index

print(numes[0]) ## output de apenas um index com a váriavel trocada 

print(numes[0:2]) ## slicing para output de dois ou mais valores (0 é a partir de qual index, e : quantos valores serão impressos após isso)

print(len(numes)) ## retorna a quantidade de indexs

print(sorted(numes, reverse=True)) ## orderna os itens em crescente ou com reverse ativo decrescente

